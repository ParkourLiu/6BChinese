	var j byte
	var k byte

	for i = 0xe0; i <= 0xef; i++ {
		//1110 0000 - 1110 1111
		for j = 0x80; j <= 0xbf; j++ {
			bb := []byte{}
			//1000 0000 - 1011 1111
			for k = 0x80; k <= 0xbf; k++ {
				//1000 0000 - 1011 1111
				bb = append(bb, i)
				bb = append(bb, j)
				bb = append(bb, k)
				if k == 0xbf {
					buf.Write(bb)
					buf.WriteString("\n")
					fmt.Println(string(bb))
				}
			}
		}
	}
